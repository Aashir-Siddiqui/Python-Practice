import math

x = min(5, 20, -2, 30)
y = max(5, 20, -2, 30)
print("Minimum value is:", x)
print("Maximum value is:", y)

a = abs(-15)
print("Absolute value is:", a)

p = pow(2, 5)  # 2 * 2 = 4, 4 * 2 = 8, 8 * 2 = 16, 16 * 2 = 32
print("2 to the power 5 is:", p)

r = math.sqrt(64)
print("Square root of 64 is:", r)

x = math.ceil(1.4)
y = math.floor(1.4)

print(x) # returns 2
print(y) # returns 1

x = math.pi

print(x)